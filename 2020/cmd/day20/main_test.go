func TestRotates(t *testing.T) {
	input := []string{
		"abc",
		"def",
		"ghi",
	}

	want := []string{
		"gda",
		"heb",
		"ifc",
	}

	got := rotates(input)
	if !reflect.DeepEqual(got, want) {
		t.Errorf("rotates(%s) = %s, want %s", input, got, want)
	}
}

